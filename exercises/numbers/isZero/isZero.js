/**
 * Returns true if the given number is 0 and false otherwise
 *
 * @param {number} num - The input number
 * @returns {boolean} True if num is 0 and false otherwise
 */
function isZero(num) {
if (num === 0){
console.log('True');
} else {
console.log('False');
}
}
console.log(isZero(0));
console.log(isZero(1));
